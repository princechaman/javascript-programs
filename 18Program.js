let Marks  = 85;

if(Marks >85 && Marks <=100){
    console.log("Grade = A");
}else if(Marks >60 && Marks <=85){
    console.log("Grade = B");
}else if(Marks >45 && Marks <=60){
    console.log("Grade = C");
}else if(Marks >36 && Marks <=45){
    console.log("Grade = D");
}
else{
    console.log("Fail");
}